import java.util.*;
public class areaofcircle {
public static void main (String [] args ){
    Scanner sc = new Scanner(System.in);
    // area of circle formula = pie * radius * radius 
    System.out.println("enter radius ");
    int r = sc.nextInt();
    double p = 3.14159;
    // calulate circumference formula = 2*pie*radius
    double a = 2 * p *  r;
    System.out.println(a);
}
    
}
    


